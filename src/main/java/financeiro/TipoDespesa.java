package financeiro;

/**
 *Enum que representa qual o tipo de despesa vai ser.
 * @author Márcio Antônio
 * @author Rafael Martins
 */
public enum TipoDespesa {

    /** Despesa relacionada ao pagamento de salários de colaboradores. */
    DESPESA_SALARIO,

    /** Despesa com materiais utilizados no funcionamento da barbearia. */
    DESPESA_MATERIAL,

    /** Despesa com produtos ou serviços de limpeza. */
    DESPESA_LIMPEZA,

    /** Despesa com brindes, promoções ou cortesias oferecidas aos clientes. */
    DESPESA_CORTESIAS;
}
